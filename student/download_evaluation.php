<?php
session_start();
require("../config/db.php");
require("../fpdf/fpdf.php");

if (!isset($_SESSION['user_id']) || $_SESSION['role'] !== 'student') {
    die("Unauthorized access.");
}

$student_id = $_SESSION['user_id'];

$sql = "SELECT e.performance_score, e.remarks, e.evaluation_date,
               s.name AS supervisor_name
        FROM evaluations e
        JOIN supervisors s ON e.supervisor_id = s.supervisor_id
        WHERE e.student_id = ?
        ORDER BY e.evaluation_date DESC
        LIMIT 1";

$stmt = $conn->prepare($sql);
$stmt->bind_param("i", $student_id);
$stmt->execute();
$result = $stmt->get_result();

if ($result->num_rows == 0) {
    die("No evaluation found.");
}

$data = $result->fetch_assoc();

$pdf = new FPDF();
$pdf->AddPage();

$pdf->SetFont('Arial','B',16);
$pdf->Cell(0,10,'Industrial Attachment Evaluation Report',0,1,'C');

$pdf->Ln(10);

$pdf->SetFont('Arial','',12);

$pdf->Cell(50,10,'Supervisor:',0,0);
$pdf->Cell(0,10,$data['supervisor_name'],0,1);

$pdf->Cell(50,10,'Performance Score:',0,0);
$pdf->Cell(0,10,$data['performance_score'],0,1);

$pdf->Cell(50,10,'Evaluation Date:',0,0);
$pdf->Cell(0,10,$data['evaluation_date'],0,1);

$pdf->Ln(10);

$pdf->SetFont('Arial','B',12);
$pdf->Cell(0,10,'Remarks:',0,1);

$pdf->SetFont('Arial','',12);
$pdf->MultiCell(0,10,$data['remarks']);

$pdf->Ln(15);

$pdf->Cell(0,10,'Generated by Industrial Attachment Management System',0,1,'C');

$pdf->Output('D','My_Evaluation_Report.pdf');
?>